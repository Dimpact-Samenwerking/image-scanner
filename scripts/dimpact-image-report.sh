    for img_dir in "$INPUT_DIR"/*/; do
        if [ -f "${img_dir}trivy-results.sarif" ]; then
            sarif_count=$((sarif_count + 1))
        fi
    done
    
    if [ $sarif_count -gt 0 ]; then
        return 0
    else
        return 1
    fi
}

